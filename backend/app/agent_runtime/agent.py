class AgentRuntime:
    """Orchestrates the retrieve → gate → generate → cite → persist pipeline.
 
    Public API:
      run_stream()  — async generator yielding SSE strings (for /chat/stream).
                                                                                                                                                                                                                                q1()    — coroutine returning a plain dict   (for /chat).
 
    Both paths share the same business logic; only the delivery layer differs.
    """
 
    async def run_once(
        self,
        question: str,
        session: AgentSession,
        identity: UserIdentity,
        top_k: int = TOP_K,
    ) -> dict:
        """Run the full pipeline and return the result as a plain dict.
 
        Returns
        -------
        dict with keys:
          answer       — generated answer text
          citations    — list of citation dicts
          thread_id    — the thread/conversation ID
          session_id   — alias for thread_id (backward compat)
        """
        user_id = identity.user_id
        user_name = identity.user_name
        thread_id = session.session_id
 
        logger.info(
            "AgentRuntime.run_once | thread=%s user=%s auth=%s | question=%s",
            thread_id, user_id, identity.auth_source, question[:120],
        )
 
        # ── 1. Resolve conversation ownership ─────────────────────────────
        if is_storage_enabled():
            ok = await _resolve_conversation(
                thread_id, user_id, user_name, session.client_provided
            )
            if not ok:
                await _persist_assistant(thread_id, user_id, _THREAD_NOT_FOUND_MSG, {}, [])
                return {
                    "answer": _THREAD_NOT_FOUND_MSG,
                    "meta": {},
                    "citations": [],
                    "thread_id": thread_id,
                    "session_id": thread_id,
                }
 
        # ── 2. Persist user message BEFORE generation ──────────────────────
        user_msg: Optional[MessageRecord] = None
        if is_storage_enabled():
            user_msg = await chat_store.append_user_message(thread_id, user_id, question)
            if user_msg is None:
                logger.error(
                    "AgentRuntime: failed to persist user message (CAS conflict or storage error) "
                    "— continuing without sequence tracking | thread=%s user=%s",
                    thread_id, user_id,
                )
 
        # -- 2a. Intent classification -- short-circuit non-questions ----
        # Load the prior assistant message so the classifier can detect
        # "yes"/"sure" replies to a bot question ("Would you like the
        # step-by-step?") and route them to RAG instead of short-circuiting.
        has_history = user_msg is not None and user_msg.sequence > 1
        prior_assistant_msg = ""
        if has_history and is_storage_enabled() and user_msg is not None:
            recent_for_intent = await chat_store.get_messages_for_user(
                thread_id, user_id, max_turns=2, before_sequence=user_msg.sequence,
            )
            for msg in reversed(recent_for_intent):
                if msg.role == "assistant" and msg.content:
                    prior_assistant_msg = msg.content
                    break
        intent, canned = classify_intent(
            question, has_history=has_history, prior_assistant_msg=prior_assistant_msg,
        )
        if canned is not None:
            logger.info(
                "Intent short-circuit | thread=%s intent=%s | question=%s",
                thread_id, intent, question,
            )
            await _persist_assistant(thread_id, user_id, canned, {}, [])
            return {
                "answer": canned,
                "message": [],
                "citations": [],
                "thread_id": thread_id,
                "session_id": thread_id,
            }
 
        # ── 2b. CONDENSATION: compress prior answer without re-retrieving ──
        # "give me in 5 steps", "summarize that", "shorter", "in one line" etc.
        # Load the last assistant answer from Cosmos and inject it directly into
        # the condensation instruction so the LLM knows exactly what to reformat
        # — even on cold start when InMemoryHistory is empty.
        if intent == INTENT_CONDENSATION:
            logger.info(
                "Intent CONDENSATION — skipping retrieval | thread=%s | question=%s",
                thread_id, question,
            )
            # Fetch the most recent assistant reply to include in the instruction
            prior_answer = ""
            if is_storage_enabled() and user_msg is not None:
                recent = await chat_store.get_messages_for_user(
                    thread_id, user_id, max_turns=2, before_sequence=user_msg.sequence,
                )
                for msg in reversed(recent):
                    if msg.role == "assistant" and msg.content:
                        prior_answer = msg.content
                        break
 
            af_session = await _get_or_create_af_session(thread_id, user_id, user_msg)
            rag_provider.store_results(af_session, [])
            rag_provider.store_condensation_mode(af_session, prior_answer=prior_answer)
            answer_text, meta, had_error = await _buffer_llm_response(question, af_session)
            if had_error:
                err_append = "\n\nI'm sorry — an error occurred. Please try again."
                answer_text = (answer_text + err_append) if answer_text else err_append.strip()
            await _persist_assistant(thread_id, user_id, answer_text, {}, [], had_error=had_error)
            return {
                "answer": answer_text,
                "meta": {},
                "citations": [],
                "meta": meta,
                "thread_id": thread_id,
                "session_id": thread_id,
            }
 
        # ── 2c. Rewrite follow-up queries using conversation context ───────
        # Use only the last 4 messages (2 Q&A pairs) so the rewriter focuses on
        # the most recent topic and doesn't blend earlier unrelated topics.
        # search_query is used ONLY for retrieval — the LLM always receives the
        # original question so it answers the user's actual words, not a keyword phrase.
        search_query = question
        if is_storage_enabled() and user_msg is not None:
            recent = await chat_store.get_messages_for_user(
                thread_id, user_id, max_turns=8, before_sequence=user_msg.sequence,
            )
            if recent:
                search_query = await asyncio.to_thread(rewrite_query, question, recent)
 
        # ── 3. RETRIEVE ────────────────────────────────────────────────────
        try:
            results, search_query_used = await _retrieve_with_fallback(
                search_query, question, top_k, thread_id,
            )
        except Exception:
            logger.exception("Retrieval failed | thread=%s", thread_id)
            err_msg = (
                "I'm sorry — an error occurred while searching the knowledge base. "
                "Please try again."
            )
            await _persist_assistant(thread_id, user_id, err_msg, {}, [], had_error=True)
            return {"answer": err_msg, "citations": [], "meta": {}, "thread_id": thread_id, "session_id": thread_id}
 
        # ── 4. GATE ────────────────────────────────────────────────────────
        avg_effective, gate_threshold, has_reranker = _compute_gate(results)
 
        if TRACE_MODE:
            logger.info(
                "TRACE | thread=%s n_results=%d avg_effective=%.4f "
                "gate=(>=%d results, >=%.3f) semantic_reranker=%s",
                thread_id, len(results), avg_effective,
                MIN_RESULTS, gate_threshold, has_reranker,
            )
 
        if len(results) < MIN_RESULTS or avg_effective < gate_threshold:
            # Log the specific reason(s) for gate failure
            reasons = []
            if len(results) < MIN_RESULTS:
                reasons.append(f"too_few_results({len(results)}<{MIN_RESULTS})")
            if avg_effective < gate_threshold:
                reasons.append(f"low_score({avg_effective:.4f}<{gate_threshold:.3f})")
            logger.warning(
                "Gate REJECTED | thread=%s reason=%s | n=%d avg=%.4f "
                "threshold_n=%d threshold=%.3f reranker=%s query=%r",
                thread_id, "+".join(reasons), len(results), avg_effective,
                MIN_RESULTS, gate_threshold, has_reranker,
                search_query_used[:120],
            )
            await _persist_assistant(thread_id, user_id, _INSUFFICIENT_EVIDENCE_MSG, {}, [])
            return {
                "answer": _INSUFFICIENT_EVIDENCE_MSG,
                "meta": {},
                "citations": [],
                "thread_id": thread_id,
                "session_id": thread_id,
            }
 
        # ── 5. AF session (cold-start hydration with before_sequence) ──────
        af_session = await _get_or_create_af_session(thread_id, user_id, user_msg)
 
        # ── 6. Inject RAG results ──────────────────────────────────────────
        rag_provider.store_results(af_session, results)
 
        # ── 7. GENERATE (buffered) ─────────────────────────────────────────
        # Always pass the original question to the LLM — search_query (rewritten)
        # is only used for retrieval. The LLM must answer the user's actual words.
        answer_text, meta, had_error = await _buffer_llm_response(question, af_session)
 
        if had_error:
            err_append = "\n\nI'm sorry — an error occurred while generating the answer. Please try again."
            answer_text = (answer_text + err_append) if answer_text else err_append.strip()
 
        # ── 8. CITE — suppress citations if LLM refused to answer ──────────
        if _is_refusal(answer_text):
            citations = []
        else:
            answer_text, cited_results = _filter_cited_results(answer_text, results)
            citations = build_citations(cited_results)
 
        # ── 9. Persist assistant message ───────────────────────────────────
        await _persist_assistant(thread_id, user_id, answer_text, citations, meta, had_error=had_error)
 
        return {
            "answer": answer_text,
            "citations": [c.model_dump() for c in citations],
            "meta": meta,
            "thread_id": thread_id,
            "session_id": thread_id,
        }
 
    async def run_stream(
        self,
        question: str,
        session: AgentSession,
        identity: UserIdentity,
        top_k: int = TOP_K,
    ) -> AsyncGenerator[str, None]:
        """Execute the pipeline and yield SSE-formatted strings.
 
        Yields
        ------
        str
            SSE strings: token data lines, named events (citations, ping),
            and the final ``[DONE]`` sentinel.
        """
        user_id = identity.user_id
        user_name = identity.user_name
        thread_id = session.session_id
        parser = TagStreamParser()

        # Initialize meta up-front so every error path in run_stream can safely
        # yield the SSE 'meta' event. Otherwise paths that fire before the LLM
        # generation (thread-not-found, intent short-circuit, retrieval error,
        # gate-rejected) hit UnboundLocalError when they try to yield meta.
        meta: dict = {}
 
        logger.info(
            "AgentRuntime.run_stream | thread=%s user=%s auth=%s | question=%s",
            thread_id, user_id, identity.auth_source, question[:120],
        )
 
        # ── 1. Resolve conversation ownership ─────────────────────────────
        if is_storage_enabled():
            ok = await _resolve_conversation(
                thread_id, user_id, user_name, session.client_provided
            )
            if not ok:
                await _persist_assistant(thread_id, user_id, _THREAD_NOT_FOUND_MSG, {}, [])
                yield _sse_data(_THREAD_NOT_FOUND_MSG)
                yield _sse_event("citations", CitationsPayload(citations=[]).model_dump_json())
                yield _sse_event("meta", json.dumps(meta, ensure_ascii=False))
                yield _sse_data("[DONE]")
                return
 
        # ── 2. Persist user message BEFORE generation ──────────────────────
        user_msg: Optional[MessageRecord] = None
        if is_storage_enabled():
            user_msg = await chat_store.append_user_message(thread_id, user_id, question)
            if user_msg is None:
                logger.error(
                    "AgentRuntime: failed to persist user message (CAS conflict or storage error) "
                    "— continuing without sequence tracking | thread=%s user=%s",
                    thread_id, user_id,
                )
 
        # -- 2a. Intent classification -- short-circuit non-questions ----
        # Load the prior assistant message so the classifier can detect
        # "yes"/"sure" replies to a bot question and route them to RAG.
        has_history = user_msg is not None and user_msg.sequence > 1
        prior_assistant_msg = ""
        if has_history and is_storage_enabled() and user_msg is not None:
            recent_for_intent = await chat_store.get_messages_for_user(
                thread_id, user_id, max_turns=2, before_sequence=user_msg.sequence,
            )
            for msg in reversed(recent_for_intent):
                if msg.role == "assistant" and msg.content:
                    prior_assistant_msg = msg.content
                    break
        intent, canned = classify_intent(
            question, has_history=has_history, prior_assistant_msg=prior_assistant_msg,
        )
        if canned is not None:
            logger.info(
                "Intent short-circuit | thread=%s intent=%s | question=%s",
                thread_id, intent, question,
            )
            await _persist_assistant(thread_id, user_id, canned, {}, [])
            yield _sse_data(canned)
            yield _sse_event("citations", CitationsPayload(citations=[]).model_dump_json())
            yield _sse_event("meta", json.dumps(meta, ensure_ascii=False))
            yield _sse_data("[DONE]")
            return
 
        # ── 2b. CONDENSATION: compress prior answer without re-retrieving ──
        # "give me in 5 steps", "summarize that", "shorter", "in one line" etc.
        # Load the last assistant answer from Cosmos and inject it directly into
        # the condensation instruction so the LLM knows exactly what to reformat.
        if intent == INTENT_CONDENSATION:
            logger.info(
                "Intent CONDENSATION — skipping retrieval | thread=%s | question=%s",
                thread_id, question,
            )
            # Fetch the most recent assistant reply to include in the instruction
            prior_answer = ""
            if is_storage_enabled() and user_msg is not None:
                recent = await chat_store.get_messages_for_user(
                    thread_id, user_id, max_turns=2, before_sequence=user_msg.sequence,
                )
                for msg in reversed(recent):
                    if msg.role == "assistant" and msg.content:
                        prior_answer = msg.content
                        break
 
            af_session = await _get_or_create_af_session(thread_id, user_id, user_msg)
            rag_provider.store_results(af_session, [])
            rag_provider.store_condensation_mode(af_session, prior_answer=prior_answer)
            last_ping_at = time.monotonic()
            answer_buf: list[str] = []
            stream_error = False
            try:
                timeout = LLM_TIMEOUT_SECONDS if LLM_TIMEOUT_SECONDS > 0 else None
                async with asyncio.timeout(timeout):
                    async for update in af_agent.run(question, stream=True, session=af_session):
                        now = time.monotonic()
                        if now - last_ping_at >= _PING_INTERVAL_SECONDS:
                            yield _sse_event("ping", "keepalive")
                            last_ping_at = now
                        if update.text:
                            chunk = update.text
                            parser.feed(chunk)
                            visible = parser.get_visible_text()
                            if visible:
                                yield _sse_data(visible)
                                parser.visible_buffer.clear()      
            except TimeoutError:
                stream_error = True
                yield _sse_data("\n\n---\n\n*Response timed out. Please try again.*")
            except Exception as exc:
                stream_error = True
                err_detail = f" ({type(exc).__name__}: {exc})" if DEBUG_MODE else ""
                yield _sse_data(f"\n\nError generating response.{err_detail}")
            finally:
                condensed = "".join(answer_buf)
                # Parse answer and meta
                answer, meta = _parse_llm_response(condensed)
                await _persist_assistant(thread_id, user_id, condensed, {}, [], had_error=stream_error)
                yield _sse_event("answer_replaced", answer)
                yield _sse_event("meta", json.dumps(meta, ensure_ascii=False))
                yield _sse_event("citations", CitationsPayload(citations=[]).model_dump_json())
                yield _sse_data("[DONE]")
            return
 
        # ── 2c. Rewrite follow-up queries using conversation context ───────
        # Use only the last 4 messages (2 Q&A pairs) so the rewriter focuses on
        # the most recent topic and doesn't blend earlier unrelated topics.
        # search_query is used ONLY for retrieval — the LLM always receives the
        # original question so it answers the user's actual words, not a keyword phrase.
        search_query = question
        if is_storage_enabled() and user_msg is not None:
            recent = await chat_store.get_messages_for_user(
                thread_id, user_id, max_turns=8, before_sequence=user_msg.sequence,
            )
            if recent:
                search_query = await asyncio.to_thread(rewrite_query, question, recent)
 
        # ── 3. RETRIEVE ────────────────────────────────────────────────────
        try:
            results, search_query_used = await _retrieve_with_fallback(
                search_query, question, top_k, thread_id,
            )
        except Exception:
            logger.exception("Retrieval failed | thread=%s", thread_id)
            err_msg = (
                "I'm sorry — an error occurred while searching the knowledge base. "
                "Please try again."
            )
            await _persist_assistant(thread_id, user_id, err_msg, {}, [], had_error=True)
            yield _sse_data(err_msg)
            yield _sse_event("citations", CitationsPayload(citations=[]).model_dump_json())
            yield _sse_event("meta", json.dumps(meta, ensure_ascii=False))
            yield _sse_data("[DONE]")
            return
 
        # ── 4. GATE ────────────────────────────────────────────────────────
        avg_effective, gate_threshold, has_reranker = _compute_gate(results)
 
        if TRACE_MODE:
            logger.info(
                "TRACE | thread=%s n_results=%d avg_effective=%.4f "
                "gate=(>=%d results, >=%.3f) semantic_reranker=%s",
                thread_id, len(results), avg_effective,
                MIN_RESULTS, gate_threshold, has_reranker,
            )
 
        if len(results) < MIN_RESULTS or avg_effective < gate_threshold:
            reasons = []
            if len(results) < MIN_RESULTS:
                reasons.append(f"too_few_results({len(results)}<{MIN_RESULTS})")
            if avg_effective < gate_threshold:
                reasons.append(f"low_score({avg_effective:.4f}<{gate_threshold:.3f})")
            logger.warning(
                "Gate REJECTED | thread=%s reason=%s | n=%d avg=%.4f "
                "threshold_n=%d threshold=%.3f reranker=%s query=%r",
                thread_id, "+".join(reasons), len(results), avg_effective,
                MIN_RESULTS, gate_threshold, has_reranker, search_query_used[:120],
            )
            await _persist_assistant(thread_id, user_id, _INSUFFICIENT_EVIDENCE_MSG, {}, [])
            yield _sse_data(_INSUFFICIENT_EVIDENCE_MSG)
            yield _sse_event("citations", CitationsPayload(citations=[]).model_dump_json())
            yield _sse_event("meta", json.dumps(meta, ensure_ascii=False))
            yield _sse_data("[DONE]")
            return
 
        # ── 5. AF session (cold-start hydration with before_sequence) ──────
        af_session = await _get_or_create_af_session(thread_id, user_id, user_msg)
 
        # ── 6. Inject RAG results ──────────────────────────────────────────
        rag_provider.store_results(af_session, results)
 
        # ── 7. GENERATE — stream tokens via Agent Framework ChatAgent ───────
        # Always pass the original question to the LLM — search_query (rewritten)
        # is only used for retrieval. The LLM must answer the user's actual words.
        last_ping_at = time.monotonic()
        answer_buf: list[str] = []
        stream_error = False
        client_disconnected = False
        try:
            timeout = LLM_TIMEOUT_SECONDS if LLM_TIMEOUT_SECONDS > 0 else None
            async with asyncio.timeout(timeout):
                async for update in af_agent.run(question, stream=True, session=af_session):
                    now = time.monotonic()
                    if now - last_ping_at >= _PING_INTERVAL_SECONDS:
                        yield _sse_event("ping", "keepalive")
                        last_ping_at = now
 
                    if update.text:
                        chunk = update.text
                        answer_buf.append(chunk)
                        parser.feed(chunk)
                        visible = parser.get_visible_text()
                        if visible:
                            yield _sse_data(visible)
                            parser.visible_buffer.clear()
 
        except TimeoutError:
            logger.error("LLM streaming timed out after %ss | thread=%s", LLM_TIMEOUT_SECONDS, thread_id)
            stream_error = True
            # Use separator to visually distinguish the error from partial answer
            yield _sse_data(
                "\n\n---\n\n*Response generation timed out. The answer above may be incomplete. Please try again.*"
            )
        except GeneratorExit:
            # Client closed connection mid-stream (browser tab closed).
            # Fall through to finally block to persist partial answer.
            client_disconnected = True
            logger.info("Client disconnected mid-stream | thread=%s", thread_id)
        except Exception as exc:
            logger.exception("LLM streaming failed | thread=%s", thread_id)
            stream_error = True
            err_detail = f" ({type(exc).__name__}: {exc})" if DEBUG_MODE else ""
            yield _sse_data(
                "\n\nI'm sorry — an error occurred while generating the answer. "
                f"Please try again.{err_detail}"
            )
        finally:
            # ── 8. CITE + PERSIST — always runs, even on client disconnect ──
            # This ensures partial answers are saved to history.
            answer_text = "".join(answer_buf)
            answer, meta = _parse_llm_response(answer_text)
            if not answer:
                # Nothing to persist (empty LLM response)
                if not client_disconnected:
                    yield _sse_data("[DONE]")
                return
 
            if _is_refusal(answer):
                citations = []
            else:
                answer_text, cited_results = _filter_cited_results(answer_text, results)
                citations = build_citations(cited_results)
 
            status = "error" if stream_error else ("partial" if client_disconnected else "complete")
            await _persist_assistant(
                thread_id, user_id, answer, meta, citations,
                had_error=stream_error or client_disconnected,
            )
 
            if not client_disconnected:
                answer, meta = _parse_llm_response(answer_text)
                yield _sse_event("answer_replaced", answer)
                yield _sse_event("meta", json.dumps(meta, ensure_ascii=False))
                yield _sse_event("citations", CitationsPayload(citations=citations).model_dump_json())
                yield _sse_data("[DONE]")
 