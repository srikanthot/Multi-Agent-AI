# Patterns that indicate the LLM declined to answer from the provided context.
# When these appear in the generated answer, citations should be suppressed
# because the documents are not actually relevant to the question.
_REFUSAL_PATTERNS = re.compile(
    r"(?:^|\. )"  # Only at start of answer or start of a sentence (not after comma)
    r"(?:"
    r"I (?:cannot|can't|am unable to|could not|couldn't) (?:answer|find|locate|provide|determine)"
    r"|(?:the |this )?(?:context|retrieved|provided) (?:blocks? )?(?:do(?:es)? not|doesn't|don't) contain"
    r"|no relevant information (?:was |is )?found"
    r"|(?:this (?:question|topic) is )?(?:outside|beyond) the scope"
    r"|not (?:covered|addressed|mentioned) in (?:the |these )?(?:context|manuals?|documents?)"
    r"|I don't have (?:enough|sufficient) (?:information|context|evidence)"
    r"|(?:unfortunately|however),? I (?:cannot|can't|could not|couldn't|don't have|do not have)"
    r"|the (?:provided |given )?(?:documents?|manuals?|context) (?:do(?:es)? not|doesn't) (?:contain|include|address|cover)"
    r")",
    re.IGNORECASE,
)
 
 
def _is_refusal(answer: str) -> bool:
    """Return True if the LLM answer indicates it could not answer the question.
 
    Uses sentence-boundary-aware regex to avoid false positives like
    'This is not related to transformer oil testing' in a valid answer.
    """
    return bool(_REFUSAL_PATTERNS.search(answer))
 
 
def _filter_cited_results(answer: str, results: list[dict]) -> tuple[str, list[dict]]:
    """Filter to only cited results and renumber references in the answer.
 
    The LLM uses [N] notation to reference context blocks (1-indexed).
    We parse those references, keep only the cited results, and renumber
    the references so they match the filtered list.
 
    Example: LLM cites [3] and [5] out of 5 chunks.
      - Filtered results: [chunk3, chunk5]
      - Answer text: [3] -> [1], [5] -> [2]
      - Sources shown: Source [1], Source [2]
 
    Returns (renumbered_answer, filtered_results).
    """
    # Only match refs in valid range (1..len(results)) to avoid false
    # positives like [2024] (year) or [404] (error code).
    max_ref = len(results)
    all_matches = re.findall(r"\[(\d+)\]", answer)
    refs = sorted(set(int(m) for m in all_matches if 1 <= int(m) <= max_ref))
    if not refs:
        return answer, results
 
    cited = [(orig_idx, r) for orig_idx, r in enumerate(results) if (orig_idx + 1) in refs]
 
    if not cited:
        return answer, results
 
    # Build old-number -> new-number mapping
    renumber_map: dict[int, int] = {}
    filtered = []
    for new_idx, (orig_idx, r) in enumerate(cited, start=1):
        renumber_map[orig_idx + 1] = new_idx
        filtered.append(r)
 
    # Renumber references in the answer text
    def _renumber(match: re.Match) -> str:
        old_num = int(match.group(1))
        new_num = renumber_map.get(old_num)
        if new_num is not None:
            return f"[{new_num}]"
        if 1 <= old_num <= max_ref:
            return ""  # Remove uncited in-range refs
        return match.group(0)  # Preserve out-of-range numbers like [2024]
 
    renumbered = re.sub(r"\[(\d+)\]", _renumber, answer)
    # Clean up double spaces left by removed references
    renumbered = re.sub(r"  +", " ", renumbered)
 
    if TRACE_MODE:
        logger.info(
            "TRACE | citation_filter: LLM cited %s out of %d chunks -> showing %d | renumber: %s",
            refs, len(results), len(filtered), renumber_map,
        )
 
    return renumbered, filtered
 
