def _compute_gate(results: list[dict]) -> tuple[float, float, bool]:
    """Return (avg_effective_score, gate_threshold, has_reranker).
 
    When the semantic reranker is active, scores are on the 0–4 scale and we
    gate on MIN_RERANKER_SCORE.  Otherwise we use base RRF/hybrid scores and
    gate on MIN_AVG_SCORE.
    """
    has_reranker = bool(results) and results[0].get("reranker_score") is not None
    if has_reranker:
        avg = sum(r.get("reranker_score") or 0 for r in results) / len(results)
        return avg, MIN_RERANKER_SCORE, True
    avg = sum(r["score"] for r in results) / len(results) if results else 0.0
    return avg, MIN_AVG_SCORE, False


def _gate_passes(results: list[dict]) -> bool:
    """Return True if results meet both the count and score thresholds."""
    if len(results) < MIN_RESULTS:
        return False
    avg, threshold, _ = _compute_gate(results)
    return avg >= threshold


async def _retrieve_with_fallback(
    search_query: str,
    original_question: str,
    top_k: int,
    thread_id: str,
) -> tuple[list[dict], str]:
    """Run retrieval, retrying once with the original question if the
    rewritten query produced results that fail the gate.

    Fallback fires only when:
      * the rewritten query differs from the user's original question, AND
      * results from the rewritten query fail the gate (count or score).

    This protects against rewriter blending: if the rewriter fused two
    unrelated topics, retrieval scores poorly on the blend and we re-run
    with the user's actual words.
    """
    results = await asyncio.to_thread(retrieve, search_query, top_k=top_k)
    if search_query == original_question:
        return results, search_query
    if _gate_passes(results):
        return results, search_query
    logger.info(
        "Retrieval gate failed on rewritten query — retrying with original | "
        "thread=%s rewrite=%r original=%r",
        thread_id, search_query[:120], original_question[:120],
    )
    fallback_results = await asyncio.to_thread(
        retrieve, original_question, top_k=top_k,
    )
    if _gate_passes(fallback_results):
        logger.info(
            "Fallback retrieval succeeded | thread=%s n=%d",
            thread_id, len(fallback_results),
        )
        return fallback_results, original_question
    return fallback_results, original_question


async def _resolve_conversation(
    thread_id: str,
    user_id: str,
    user_name: str,
    client_provided: bool,
) -> bool:
    """Ensure a conversation record exists for (thread_id, user_id).
 
    Returns True if the pipeline may continue, False if it should abort.
 
    Behavior:
      - client_provided=True:  the conversation MUST already exist for this
        user.  If not found, log a warning and return False.  (routes.py
        should have caught this first; this is a defensive second layer.)
      - client_provided=False: the thread_id was auto-generated by AgentSession.
        If the conversation does not exist yet, create it now.  This preserves
        backward compatibility for callers that do not pre-create via
        POST /conversations.
    """
    conv = await chat_store.get_conversation(thread_id, user_id)
 
    if conv is not None:
        logger.info(
            "AgentRuntime: conversation resolved | thread=%s user=%s turns=%d",
            thread_id, user_id, conv.message_count,
        )
        return True
 
    if client_provided:
        # Thread not found for this user — unauthorized access or stale ID.
        logger.warning(
            "AgentRuntime: client-provided thread_id not found for user — "
            "aborting | thread=%s user=%s",
            thread_id, user_id,
        )
        return False
 
    # Auto-generated thread_id — create the conversation record.
    created = await chat_store.create_conversation(thread_id, user_id, user_name)
    if created is None:
        logger.error(
            "AgentRuntime: failed to create conversation | thread=%s user=%s",
            thread_id, user_id,
        )
        return False
 
    logger.info(
        "AgentRuntime: conversation auto-created | thread=%s user=%s",
        thread_id, user_id,
    )
    return True
 
 
async def _get_or_create_af_session(
    thread_id: str,
    user_id: str,
    current_user_msg: Optional[MessageRecord],
) -> AFAgentSession:
    """Return the AF session for thread_id, hydrating history on cold start.
 
    Cold-start behavior:
      - A new AFAgentSession is created.
      - Prior Cosmos messages are loaded using get_messages_for_user() with
        before_sequence to exclude the just-persisted user message.  This
        guarantees the current question never appears in both the injected
        history block and the active prompt.
      - get_messages_for_user() validates conversation ownership before
        querying and filters results by user_id, so history can only ever
        be loaded for the conversation's owning user.
 
    Warm session behavior:
      - The existing AFAgentSession is returned as-is.
      - InMemoryHistoryProvider already holds the in-process turn history.
      - No Cosmos history is loaded or injected.
    """
    existing = _af_sessions.get(thread_id)
    if existing is not None:
        # Move to end so this session is treated as most-recently-used.
        _af_sessions.move_to_end(thread_id)
        logger.info(
            "AgentRuntime: warm session reused | thread=%s", thread_id
        )
        return existing
 
    # Cold start — create fresh AF session
    af_session = af_agent.create_session()
 
    # Evict least-recently-used entry if the cache is full.
    if len(_af_sessions) >= _AF_SESSION_MAX_SIZE:
        lru_key, _ = _af_sessions.popitem(last=False)
        logger.info("AgentRuntime: evicted LRU session from cache | evicted=%s", lru_key)
 
    _af_sessions[thread_id] = af_session
    logger.info("AgentRuntime: cold-start — new AF session | thread=%s", thread_id)
 
    if not is_storage_enabled():
        return af_session
 
    # Determine sequence boundary so the current user question is excluded
    before_seq: Optional[int] = None
    if current_user_msg is not None:
        before_seq = current_user_msg.sequence
        logger.info(
            "AgentRuntime: cold-start history will exclude seq>=%d (current user msg) | thread=%s",
            before_seq, thread_id,
        )
 
    # get_messages_for_user validates ownership and filters by user_id
    prior = await chat_store.get_messages_for_user(
        thread_id,
        user_id,
        max_turns=COSMOS_HISTORY_MAX_TURNS,
        before_sequence=before_seq,
    )
 
    if prior:
        block = format_history_block(prior)
        history_provider.store_history_block(af_session, block)
        logger.info(
            "AgentRuntime: injected %d prior message(s) into cold-start session "
            "(before_seq=%s) | thread=%s",
            len(prior), before_seq, thread_id,
        )
    else:
        logger.info(
            "AgentRuntime: no prior history to inject (before_seq=%s) | thread=%s",
            before_seq, thread_id,
        )
 
    return af_session
 
META_RE = re.compile(r"<meta>(.*?)</meta>", re.DOTALL)
ANSWER_RE = re.compile(r"<answer>(.*?)</answer>", re.DOTALL)
 
def _parse_llm_response(raw: str):
    # Extract the meta block
    meta_match = META_RE.search(raw)
    meta = None
    if meta_match:
        try:
            meta = json.loads(meta_match.group(1).strip())
        except json.JSONDecodeError:
            meta = {"error": "invalid_meta_json"}
 
    # Extract the answer block
    answer_match = ANSWER_RE.search(raw)
    if answer_match:
        answer = answer_match.group(1).strip()
    else:
        # fallback: treat whole text as answer
        answer = raw.strip()
 
    return answer, meta
 
async def _buffer_llm_response(
    question: str,
    af_session: AFAgentSession,
) -> tuple[str, bool]:
    """Run the LLM and buffer all streamed tokens.
 
    Returns (answer_text, had_error).  Uses stream=True internally so the
    code path is identical to run_stream(); only the delivery mechanism
    differs (buffered vs. yielded).
    """
    buf: list[str] = []
    had_error = False
    try:
        timeout = LLM_TIMEOUT_SECONDS if LLM_TIMEOUT_SECONDS > 0 else None
        async with asyncio.timeout(timeout):
            async for update in af_agent.run(question, stream=True, session=af_session):
                if update.text:
                    buf.append(update.text)
    except TimeoutError:
        logger.error("LLM generation timed out after %ss during buffered run", LLM_TIMEOUT_SECONDS)
        had_error = True
    except Exception:
        logger.exception("LLM generation failed during buffered run")
        had_error = True
    raw = "".join(buf)
    answer, meta = _parse_llm_response(raw)
    return answer, meta, had_error
 
 
async def _persist_assistant(
    thread_id: str,
    user_id: str,
    answer_text: str,
    meta: Optional[dict],
    citations: list[Citation],
    had_error: bool = False,
) -> None:
    """Persist the assistant message and citations to Cosmos."""
    if not is_storage_enabled() or not answer_text:
        return
    status = "error" if had_error else "complete"
    citations_dicts = [c.model_dump() for c in citations]
    await chat_store.append_assistant_message(
        thread_id, user_id, answer_text, citations=citations_dicts, status=status, meta=meta
    )
    logger.info(
        "AgentRuntime: assistant message persisted | thread=%s citations=%d status=%s",
        thread_id, len(citations_dicts), status,
    )
 
 
